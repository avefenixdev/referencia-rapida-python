a = 22
b = 2

print(a + b) # Suma -> 24
print(a - b) # Resta -> 20
print(a * b) # Multiplicación -> 44
print(a / b) # División (float) -> 11.0
print(a // b) # División entera -> 11
print(a % b) # Módulo (resto) -> 0
print(a ** b) # Potencia -> 484

